league_avg = df['HOME_WIN_INT'].mean()
df['HOME_ADVANTAGE_SCORE'] = df['HOME_COURT_STRENGTH'] - league_avg
df['STRENGTH_DIFF']        = df['HOME_COURT_STRENGTH'] - df['AWAY_TEAM_STRENGTH']

# ── Defensive rolling ────────────────────────────────────────
home_def = df[['GAME_DATE','GAME_ID','HOME_TEAM','AWAY_PTS']].rename(
    columns={'HOME_TEAM':'TEAM','AWAY_PTS':'OPP_PTS'})
away_def = df[['GAME_DATE','GAME_ID','AWAY_TEAM','HOME_PTS']].rename(
    columns={'AWAY_TEAM':'TEAM','HOME_PTS':'OPP_PTS'})
all_def  = pd.concat([home_def,away_def],ignore_index=True).sort_values(
    ['TEAM','GAME_DATE']).reset_index(drop=True)
all_def['DEF_ROLL10'] = all_def.groupby('TEAM')['OPP_PTS'].transform(
    lambda x: x.shift(1).rolling(10,min_periods=3).mean())
def_lkp = all_def[['GAME_ID','TEAM','DEF_ROLL10']].drop_duplicates()
df = df.merge(def_lkp.rename(columns={'TEAM':'HOME_TEAM',
    'DEF_ROLL10':'HOME_DEF_ROLL10'}),on=['GAME_ID','HOME_TEAM'],how='left')
df = df.merge(def_lkp.rename(columns={'TEAM':'AWAY_TEAM',
    'DEF_ROLL10':'AWAY_DEF_ROLL10'}),on=['GAME_ID','AWAY_TEAM'],how='left')
df['COMBINED_DEF_ROLL10'] = df['HOME_DEF_ROLL10'] + df['AWAY_DEF_ROLL10']
df['EXPECTED_TOTAL'] = (
    df['HOME_ROLL10_PTS'] + df['AWAY_ROLL10_PTS'] +
    df['HOME_DEF_ROLL10'] + df['AWAY_DEF_ROLL10']) / 2

# ── EWM features ─────────────────────────────────────────────
print("Building EWM features...")
ewm_map = {
    'HOME_TEAM':[('HOME_PTS','HOME_EWM_PTS'),
                 ('HOME_FG_PCT','HOME_EWM_FG_PCT'),
                 ('HOME_FG3_PCT','HOME_EWM_FG3_PCT'),
                 ('AWAY_PTS','HOME_EWM_DEF')],
    'AWAY_TEAM':[('AWAY_PTS','AWAY_EWM_PTS'),
                 ('AWAY_FG_PCT','AWAY_EWM_FG_PCT'),
                 ('AWAY_FG3_PCT','AWAY_EWM_FG3_PCT'),
                 ('HOME_PTS','AWAY_EWM_DEF')]
}
for team_col, pairs in ewm_map.items():
    grp = df.groupby(team_col)
    for src, new in pairs:
        df[new] = grp[src].transform(
            lambda x: x.shift(1).ewm(span=10,min_periods=3).mean())

df['EWM_EXPECTED_TOTAL'] = (
    df['HOME_EWM_PTS'] + df['AWAY_EWM_PTS'] +
    df['HOME_EWM_DEF'] + df['AWAY_EWM_DEF']) / 2
df['HOME_MOMENTUM'] = df['HOME_EWM_PTS'] - df['HOME_ROLL10_PTS']
df['AWAY_MOMENTUM'] = df['AWAY_EWM_PTS'] - df['AWAY_ROLL10_PTS']
df['COMBINED_MOMENTUM'] = df['HOME_MOMENTUM'] + df['AWAY_MOMENTUM']

# ════════════════════════════════════════════════════════════
# NEW: STRENGTH OF SCHEDULE NORMALIZATION
# ════════════════════════════════════════════════════════════
print("Building Strength of Schedule features...")

# ── Step 1: League average scoring per season ────────────────
# This is our baseline — what does an average team score?
league_avg_pts = df.groupby('SEASON')['HOME_PTS'].mean().to_dict()
league_avg_def = df.groupby('SEASON')['AWAY_PTS'].mean().to_dict()

df['LEAGUE_AVG_PTS'] = df['SEASON'].map(league_avg_pts)

# ── Step 2: Rolling opponent defensive strength ──────────────
# For each game, what was the rolling avg defensive rating
# of all opponents this team has faced in last 10 games?
# This is the SoS — harder schedule = faced better defenses

# Build opponent pts-allowed lookup
# When team plays home: opponent is AWAY team
# When team plays away: opponent is HOME team

all_app2 = pd.concat([
    df[['GAME_DATE','GAME_ID','HOME_TEAM','AWAY_DEF_ROLL10']].rename(
        columns={'HOME_TEAM':'TEAM','AWAY_DEF_ROLL10':'OPP_DEF'}),
    df[['GAME_DATE','GAME_ID','AWAY_TEAM','HOME_DEF_ROLL10']].rename(
        columns={'AWAY_TEAM':'TEAM','HOME_DEF_ROLL10':'OPP_DEF'})
], ignore_index=True).sort_values(['TEAM','GAME_DATE'])

# Rolling avg of opponent defensive strength faced
all_app2['OPP_DEF_ROLL10'] = all_app2.groupby('TEAM')['OPP_DEF'].transform(
    lambda x: x.shift(1).rolling(10, min_periods=3).mean())

sos_lkp = all_app2[['GAME_ID','TEAM','OPP_DEF_ROLL10']].drop_duplicates()

# Merge — home team's schedule strength
df = df.merge(
    sos_lkp.rename(columns={
        'TEAM':'HOME_TEAM',
        'OPP_DEF_ROLL10':'HOME_SOS_DEF'}),
    on=['GAME_ID','HOME_TEAM'], how='left')

# Away team's schedule strength
df = df.merge(
    sos_lkp.rename(columns={
        'TEAM':'AWAY_TEAM',
        'OPP_DEF_ROLL10':'AWAY_SOS_DEF'}),
    on=['GAME_ID','AWAY_TEAM'], how='left')

# ── Step 3: Adjusted offensive rating ───────────────────────
# This is the key formula:
# Adjusted Score = Raw Score × (League Avg Defense / Opponent Defense)
#
# If opponent defense allows 103 pts (strong) and league avg is 114:
# Adjustment factor = 114/103 = 1.107
# A 112pt game becomes 112 × 1.107 = 124 adjusted
#
# If opponent defense allows 124 pts (weak) and league avg is 114:
# Adjustment factor = 114/124 = 0.919
# A 118pt game becomes 118 × 0.919 = 108 adjusted

# Compute adjusted rolling offensive rating
# We adjust each team's rolling pts by the avg defensive quality faced

df['HOME_ADJ_OFF'] = df['HOME_ROLL10_PTS'] * (
    df['LEAGUE_AVG_PTS'] / df['HOME_SOS_DEF'].clip(lower=90))

df['AWAY_ADJ_OFF'] = df['AWAY_ROLL10_PTS'] * (
    df['LEAGUE_AVG_PTS'] / df['AWAY_SOS_DEF'].clip(lower=90))

# Adjusted defensive rating
# How good is this team's defense adjusted for opponent quality?
df['HOME_ADJ_DEF'] = df['HOME_DEF_ROLL10'] * (
    df['LEAGUE_AVG_PTS'] / df['HOME_SOS_DEF'].clip(lower=90))

df['AWAY_ADJ_DEF'] = df['AWAY_DEF_ROLL10'] * (
    df['LEAGUE_AVG_PTS'] / df['AWAY_SOS_DEF'].clip(lower=90))

# ── Step 4: SoS-adjusted expected total ─────────────────────
# The matchup expected score using adjusted ratings
# Home team's adjusted offense vs Away team's adjusted defense
# Away team's adjusted offense vs Home team's adjusted defense

df['SOS_EXPECTED_HOME'] = (
    df['HOME_ADJ_OFF'] + df['AWAY_ADJ_DEF']) / 2

df['SOS_EXPECTED_AWAY'] = (
    df['AWAY_ADJ_OFF'] + df['HOME_ADJ_DEF']) / 2

df['SOS_EXPECTED_TOTAL'] = (
    df['SOS_EXPECTED_HOME'] + df['SOS_EXPECTED_AWAY'])

# Schedule difficulty differential
# Positive = home team faced harder schedule
df['SOS_DIFF'] = df['HOME_SOS_DEF'] - df['AWAY_SOS_DEF']

print(f"✅ SoS features built")

# ── Drop early rows without enough history ───────────────────
games_clean = df.dropna(subset=[
    'HOME_ROLL10_PTS','AWAY_ROLL10_PTS',
    'HOME_ADJ_OFF','AWAY_ADJ_OFF'
]).copy().reset_index(drop=True)

print(f"\n{'='*55}")
print(f"DATASET SUMMARY")
print(f"{'='*55}")
print(f"Total games        : {len(games_clean)}")
print(f"Season breakdown   :")
print(games_clean['SEASON'].value_counts().sort_index().to_string())

# ── Preview SoS impact ───────────────────────────────────────
print(f"\nSoS normalization sample — does it make sense?")
print(f"(Higher ADJ_OFF = better offense vs harder schedule)\n")

s2526 = games_clean[games_clean['SEASON']=='2025-26']
team_adj = s2526.groupby('HOME_TEAM').agg(
    RAW_OFF  = ('HOME_ROLL10_PTS','mean'),
    ADJ_OFF  = ('HOME_ADJ_OFF','mean'),
    SOS_DEF  = ('HOME_SOS_DEF','mean'),
).round(1).sort_values('ADJ_OFF', ascending=False)

print("Top 8 teams by Adjusted Offensive Rating (2025-26):")
print(team_adj.head(8).to_string())
print("\nBottom 5 teams:")
print(team_adj.tail(5).to_string())

