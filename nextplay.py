# CELL 14 — Exponential Weighting + Final Model
# ============================================================
# Simple rolling average treats game 1 and game 10 equally.
# Exponentially weighted average gives more weight to recent
# games — last game counts more than game from 3 weeks ago.
#
# This is especially useful mid-season when teams are
# hot or cold — the model reacts faster to current form.
# ============================================================

def add_ewm_features(games, span=10):
    """
    Exponentially weighted moving averages.
    span=10 means roughly last 10 games, recent weighted more.
    """
    df = games.copy().sort_values('GAME_DATE').reset_index(drop=True)

    # Stats to apply EWM to
    ewm_stats = {
        'HOME_TEAM': [
            ('HOME_PTS',     'HOME_EWM_PTS'),
            ('HOME_FG_PCT',  'HOME_EWM_FG_PCT'),
            ('HOME_FG3_PCT', 'HOME_EWM_FG3_PCT'),
            ('AWAY_PTS',     'HOME_EWM_DEF'),    # pts allowed at home
        ],
        'AWAY_TEAM': [
            ('AWAY_PTS',     'AWAY_EWM_PTS'),
            ('AWAY_FG_PCT',  'AWAY_EWM_FG_PCT'),
            ('AWAY_FG3_PCT', 'AWAY_EWM_FG3_PCT'),
            ('HOME_PTS',     'AWAY_EWM_DEF'),    # pts allowed on road
        ]
    }

    for team_col, stat_pairs in ewm_stats.items():
        group = df.groupby(team_col)
        for source_col, new_col in stat_pairs:
            df[new_col] = (
                group[source_col]
                .transform(lambda x:
                    x.shift(1).ewm(span=span, min_periods=3).mean()
                )
            )

    # Rebuild EXPECTED_TOTAL using EWM instead of simple rolling
    df['EWM_EXPECTED_TOTAL'] = (
        df['HOME_EWM_PTS']  + df['AWAY_EWM_PTS'] +
        df['HOME_EWM_DEF']  + df['AWAY_EWM_DEF']
    ) / 2

    # Momentum signal — is EWM higher than rolling avg?
    # Positive = team is scoring MORE recently than their average
    df['HOME_MOMENTUM'] = df['HOME_EWM_PTS']  - df['HOME_ROLL10_PTS']
    df['AWAY_MOMENTUM'] = df['AWAY_EWM_PTS']  - df['AWAY_ROLL10_PTS']

    # Combined momentum
    df['COMBINED_MOMENTUM'] = df['HOME_MOMENTUM'] + df['AWAY_MOMENTUM']

    return df

