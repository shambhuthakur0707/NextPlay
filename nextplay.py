def evaluate_model(model, X_test, y_test, name):
    """Print a full evaluation report for one model."""
    preds = model.predict(X_test)
    mae   = mean_absolute_error(y_test, preds)
    bias  = np.mean(preds - y_test)
    within_5  = np.mean(np.abs(preds - y_test) <= 5)  * 100
    within_10 = np.mean(np.abs(preds - y_test) <= 10) * 100
    within_15 = np.mean(np.abs(preds - y_test) <= 15) * 100

    print(f"\n  {name}")
    print(f"    MAE          : {mae:.2f} pts")
    print(f"    Bias         : {bias:+.2f} pts")
    print(f"    Within  5 pts: {within_5:.1f}%")
    print(f"    Within 10 pts: {within_10:.1f}%")
    print(f"    Within 15 pts: {within_15:.1f}%")
    return preds, mae

