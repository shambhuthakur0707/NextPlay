# ============================================================
# CELL 5 — Rolling Feature Engineering
# ============================================================
# For every game we calculate each team's recent form
# using their LAST 10 games before that game.
#
# Why 10 games? Enough to show real trends without being
# so long it includes form from 2 months ago.
#
# KEY RULE: we always shift(1) before rolling — this means
# we never include the current game in its own features.
# That would be cheating (data leakage).
# ============================================================

def add_rolling_features(games, window=10):
    """
    For each game, adds rolling averages of the last N games
    for both home and away teams separately.
    """

    # Stats we want rolling averages for
    home_stats = [
        'HOME_PTS', 'HOME_FG_PCT', 'HOME_FG3_PCT', 'HOME_FT_PCT',
        'HOME_FG3A', 'HOME_FTA', 'HOME_AST', 'HOME_TOV',
        'HOME_OREB', 'HOME_REB', 'HOME_PF', 'TOTAL_PTS'
    ]
    away_stats = [
        'AWAY_PTS', 'AWAY_FG_PCT', 'AWAY_FG3_PCT', 'AWAY_FT_PCT',
        'AWAY_FG3A', 'AWAY_FTA', 'AWAY_AST', 'AWAY_TOV',
        'AWAY_OREB', 'AWAY_REB', 'AWAY_PF'
    ]

    df = games.copy()

    # ── HOME TEAM rolling features ──────────────────────────
    # Group by home team, then for each stat:
    # shift(1) skips current game, rolling(10) averages last 10
    home_group = df.groupby('HOME_TEAM')

    for stat in home_stats:
        col_name = f'HOME_ROLL10_{stat.replace("HOME_","").replace("AWAY_","")}'
        df[col_name] = (
            home_group[stat]
            .transform(lambda x: x.shift(1).rolling(window, min_periods=3).mean())
        )

    # ── AWAY TEAM rolling features ──────────────────────────
    away_group = df.groupby('AWAY_TEAM')

    for stat in away_stats:
        col_name = f'AWAY_ROLL10_{stat.replace("AWAY_","").replace("HOME_","")}'
        df[col_name] = (
            away_group[stat]
            .transform(lambda x: x.shift(1).rolling(window, min_periods=3).mean())
        )

    # ── Derived features (combine home + away signals) ──────
    # Predicted pace — how many total points do both teams avg?
    df['COMBINED_PTS_ROLL10']  = df['HOME_ROLL10_PTS'] + df['AWAY_ROLL10_PTS']

    # Shooting volume — 3PT attempts signal pace & style
    df['COMBINED_FG3A_ROLL10'] = df['HOME_ROLL10_FG3A'] + df['AWAY_ROLL10_FG3A']

    # Free throw volume — more FTA = slower, foul-heavy game
    df['COMBINED_FTA_ROLL10']  = df['HOME_ROLL10_FTA'] + df['AWAY_ROLL10_FTA']

    # Turnover diff — turnovers hurt scoring
    df['TOV_DIFF_ROLL10']      = df['HOME_ROLL10_TOV'] - df['AWAY_ROLL10_TOV']

    # Rebounding edge — more offensive rebounds = more possessions
    df['OREB_DIFF_ROLL10']     = df['HOME_ROLL10_OREB'] - df['AWAY_ROLL10_OREB']

    return df

