# CELL 7 — Home Court + Season Context Features
# ============================================================
# Home court advantage is real in the NBA — home teams score
# more and win more. We encode this explicitly.
#
# Season context captures WHERE in the season the game is:
# - Early season: teams still adjusting, scores unpredictable
# - Mid season: most predictable, teams in rhythm
# - Late season: load management, tanking, resting stars
# ============================================================

def add_context_features(games):
    df = games.copy()

    # ── SEASON CONTEXT ──────────────────────────────────────
    # What game number is this in the season for each team?
    # We use this to know if it's early/mid/late season

    df = df.sort_values('GAME_DATE').reset_index(drop=True)

    # Game number within each season (1 = first game, 82 = last)
    df['SEASON_GAME_NUM'] = df.groupby(
        ['SEASON','HOME_TEAM']
    ).cumcount() + 1

    # Season stage — which third of the season are we in?
    # 1 = early (games 1-27), 2 = mid (28-55), 3 = late (56-82)
    df['SEASON_STAGE'] = pd.cut(
        df['SEASON_GAME_NUM'],
        bins=[0, 27, 55, 82],
        labels=[1, 2, 3]
    ).astype(float)

    # ── HOME COURT FEATURES ─────────────────────────────────
    # Historical home win rate per team (rolling, before this game)
    # Teams like BOS and DEN have much stronger home court than others

    df['HOME_WIN_INT'] = (df['HOME_WL'] == 'W').astype(int)

    df['HOME_COURT_STRENGTH'] = (
        df.groupby('HOME_TEAM')['HOME_WIN_INT']
        .transform(lambda x: x.shift(1).rolling(20, min_periods=5).mean())
    )

    # League average home win rate (for comparison)
    league_home_winrate = df['HOME_WIN_INT'].mean()

    # How much better/worse is this team at home vs league avg?
    df['HOME_ADVANTAGE_SCORE'] = df['HOME_COURT_STRENGTH'] - league_home_winrate

    # ── OPPONENT STRENGTH ────────────────────────────────────
    # Rolling win rate of the away team (how strong is the visitor?)
    df['AWAY_WIN_INT'] = (df['AWAY_WL'] == 'W').astype(int)

    df['AWAY_TEAM_STRENGTH'] = (
        df.groupby('AWAY_TEAM')['AWAY_WIN_INT']
        .transform(lambda x: x.shift(1).rolling(20, min_periods=5).mean())
    )

    # Strength matchup — positive means home team is stronger
    df['STRENGTH_DIFF'] = (
        df['HOME_COURT_STRENGTH'] - df['AWAY_TEAM_STRENGTH']
    )

    return df

