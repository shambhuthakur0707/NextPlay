# CELL 24 — Garbage Time Filter + OT Detection
# ============================================================
# PROBLEM 1: GARBAGE TIME
# When a game is a 40pt blowout in Q4, the losing team's
# starters sit. Reserves play. Scores inflate meaninglessly.
# Our model trains on these inflated scores and learns
# the wrong patterns.
#
# Fix: Flag and remove games with margin > 25 pts from
# TRAINING data. We still predict these games — we just
# don't let them corrupt our model's learning.
#
# PROBLEM 2: OVERTIME GAMES
# OT adds 5-25 extra points. No model predicts OT reliably.
# Training on OT games teaches the model that some games
# score 280+ pts — which misleads predictions on normal games.
#
# Fix: Flag OT games and remove from training.
# We detect OT by: total points > 240 AND margin < 8
# (close high-scoring game = likely OT)
# ============================================================

print("Analyzing game distribution before filtering...\n")

