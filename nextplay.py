# CELL 10 — Add Defensive Features
# ============================================================
# Right now our model only knows how much each team SCORES.
# It doesn't know how much each team ALLOWS.
#
# Defensive rating = points allowed per 100 possessions
# We approximate it from our data:
# points the OPPONENT scored against this team (last 10 games)
#
# This is the single biggest missing signal for total pts.
# ============================================================

def add_defensive_features(games, window=10):
    df = games.copy().sort_values('GAME_DATE').reset_index(drop=True)

    # For each home game: points the away team scored = pts allowed by home
    # For each away game: points the home team scored = pts allowed by away

    # Build opponent-scored lookup
    home_def = df[['GAME_DATE','GAME_ID','HOME_TEAM','AWAY_PTS']].copy()
    home_def.columns = ['GAME_DATE','GAME_ID','TEAM','OPP_PTS']

    away_def = df[['GAME_DATE','GAME_ID','AWAY_TEAM','HOME_PTS']].copy()
    away_def.columns = ['GAME_DATE','GAME_ID','TEAM','OPP_PTS']

    all_def = pd.concat([home_def, away_def], ignore_index=True)
    all_def = all_def.sort_values(['TEAM','GAME_DATE']).reset_index(drop=True)

    # Rolling avg of points allowed (shift first — no leakage)
    all_def['DEF_ROLL10'] = (
        all_def.groupby('TEAM')['OPP_PTS']
        .transform(lambda x: x.shift(1).rolling(window, min_periods=3).mean())
    )

    def_lookup = all_def[['GAME_ID','TEAM','DEF_ROLL10']].drop_duplicates()

    # Merge home defense (how many pts does home team allow?)
    df = df.merge(
        def_lookup.rename(columns={
            'TEAM'      : 'HOME_TEAM',
            'DEF_ROLL10': 'HOME_DEF_ROLL10'
        }),
        on=['GAME_ID','HOME_TEAM'], how='left'
    )

    # Merge away defense
    df = df.merge(
        def_lookup.rename(columns={
            'TEAM'      : 'AWAY_TEAM',
            'DEF_ROLL10': 'AWAY_DEF_ROLL10'
        }),
        on=['GAME_ID','AWAY_TEAM'], how='left'
    )

    # Combined defensive signal
    # Low = defensive game expected, High = shootout expected
    df['COMBINED_DEF_ROLL10'] = df['HOME_DEF_ROLL10'] + df['AWAY_DEF_ROLL10']

    # Pace indicator:
    # avg of what each team scores + what each team allows
    df['EXPECTED_TOTAL'] = (
        df['HOME_ROLL10_PTS'] + df['AWAY_ROLL10_PTS'] +
        df['HOME_DEF_ROLL10'] + df['AWAY_DEF_ROLL10']
    ) / 2

    return df

