def calculate_confidence(home_team, away_team, margin):
    """
    Better confidence scoring based on:
    - Team scoring consistency (std dev of last 10 games)
    - Season stage
    - Score margin
    Rather than just margin alone.
    """
    # Get scoring variance for both teams
    home_games = model_df[
        (model_df['HOME_TEAM']==home_team) &
        (model_df['SEASON']=='2024-25')
    ].tail(15)

    away_games = model_df[
        (model_df['AWAY_TEAM']==away_team) &
        (model_df['SEASON']=='2024-25')
    ].tail(15)

    # Standard deviation of recent scoring = unpredictability
    home_std = home_games['HOME_PTS'].std() if len(home_games)>5 else 12
    away_std  = away_games['AWAY_PTS'].std()  if len(away_games)>5 else 12

    avg_std = (home_std + away_std) / 2

    # Low std = consistent team = more predictable
    # High std = boom/bust team = less predictable
    if avg_std < 9 and abs(margin) >= 8:
        return "HIGH"
    elif avg_std < 11 and abs(margin) >= 5:
        return "MEDIUM"
    elif avg_std < 13:
        return "LOW"
    else:
        return "SKIP — high variance teams"

def overtime_risk(pred_margin):
    """
    Estimate overtime probability from predicted margin.
    Close games are more likely to go to OT.
    """
    abs_margin = abs(pred_margin)
    if abs_margin <= 2:   return "HIGH OT risk — game too close"
    elif abs_margin <= 5: return "MEDIUM OT risk"
    else:                 return "Low OT risk"

def blowout_risk(home_streak, away_streak, strength_diff):
    """
    Flag games at risk of becoming blowouts.
    Large streak + strength difference = potential blowout.
    """
    if abs(home_streak) >= 7 or abs(away_streak) >= 7:
        return "⚠️  Blowout risk — large momentum gap"
    elif abs(strength_diff) > 0.3:
        return "⚠️  Blowout risk — large quality gap"
