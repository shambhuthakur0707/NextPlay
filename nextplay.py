# One API call per team per season = 60 calls total.
# Much faster than per-game pulls.
#
# Zones we care about:
#   - Restricted area (paint) — high efficiency, slow pace
#   - 3PT left corner         — most efficient 3
#   - 3PT right corner        — most efficient 3
#   - 3PT above break         — volume 3s
#   - Mid-range               — low efficiency
# ============================================================

from nba_api.stats.endpoints import shotchartdetail
from nba_api.stats.static import teams as nba_teams_static

def get_team_shot_profile(team_id, team_abbr, season):
    """
    Pull shot chart for one team for one season.
    Returns a dict of shot zone percentages.
    """
    try:
        shot_data = shotchartdetail.ShotChartDetail(
            team_id=team_id,
            player_id=0,          # 0 = all players (team total)
            season_nullable=season,
            season_type_all_star='Regular Season',
            context_measure_simple='FGA'
        ).get_data_frames()[0]

        if len(shot_data) == 0:
            return None

        total_shots = len(shot_data)

        # Count shots by zone
        zone_counts = shot_data['SHOT_ZONE_BASIC'].value_counts()

        # Calculate percentages for each zone
        paint_shots    = zone_counts.get('Restricted Area', 0) + \
                         zone_counts.get('In The Paint (Non-RA)', 0)
        threept_shots  = zone_counts.get('Left Corner 3', 0) + \
                         zone_counts.get('Right Corner 3', 0) + \
                         zone_counts.get('Above the Break 3', 0)
        midrange_shots = zone_counts.get('Mid-Range', 0)

        paint_rate    = paint_shots    / total_shots
        threept_rate  = threept_shots  / total_shots
        midrange_rate = midrange_shots / total_shots

        # Shot accuracy by zone
        made = shot_data[shot_data['SHOT_MADE_FLAG'] == 1]
        made_zones = made['SHOT_ZONE_BASIC'].value_counts()

        paint_made   = made_zones.get('Restricted Area', 0) + \
                       made_zones.get('In The Paint (Non-RA)', 0)
        threept_made = made_zones.get('Left Corner 3', 0) + \
                       made_zones.get('Right Corner 3', 0) + \
                       made_zones.get('Above the Break 3', 0)

        paint_pct    = paint_made    / paint_shots    if paint_shots    > 0 else 0
        threept_pct  = threept_made  / threept_shots  if threept_shots  > 0 else 0

        # Points per shot attempt by zone (efficiency)
        # 3PT worth 3 pts, paint/mid worth 2 pts
        pts_per_shot = (
            (threept_made * 3) + (paint_made * 2) +
            (made_zones.get('Mid-Range', 0) * 2)
        ) / total_shots

        return {
            'TEAM_ABBR'       : team_abbr,
            'SEASON'          : season,
            'TOTAL_SHOTS'     : total_shots,
            'PAINT_RATE'      : round(paint_rate, 4),
            'THREEPT_RATE'    : round(threept_rate, 4),
            'MIDRANGE_RATE'   : round(midrange_rate, 4),
            'PAINT_PCT'       : round(paint_pct, 4),
            'THREEPT_PCT'     : round(threept_pct, 4),
            'PTS_PER_SHOT'    : round(pts_per_shot, 4),
        }

    except Exception as e:
        print(f"    ❌ Error: {e}")
        return None


