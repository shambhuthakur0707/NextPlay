def predict_and_log(home_team, away_team,
                    game_date=None,
                    home_out=None, away_out=None,
                    actual_home=None, actual_away=None,
                    verbose=True):
    """
    Makes a prediction AND logs it.
    If actual scores provided, calculates error immediately.
    Otherwise logs prediction — fill actuals after game.

    Args:
        home_team    : e.g. 'BOS'
        away_team    : e.g. 'LAL'
        game_date    : 'YYYY-MM-DD' string
        home_out     : injured home players e.g. ['Tatum']
        away_out     : injured away players e.g. ['LeBron']
        actual_home  : actual home score (if known)
        actual_away  : actual away score (if known)
    """
    global pred_log

    home_out  = home_out  or []
    away_out  = away_out  or []
    game_date = game_date or datetime.today().strftime('%Y-%m-%d')

    # ── Get prediction ───────────────────────────────────────
    result = predict_game(
        home_team, away_team,
        home_out=home_out,
        away_out=away_out,
        verbose=verbose
    )

    if result is None:
        return None

    # ── Calculate errors if actuals provided ─────────────────
    err_home  = None
    err_away  = None
    err_total = None
    abs_home  = None
    abs_away  = None
    abs_total = None

    if actual_home is not None and actual_away is not None:
        actual_total = actual_home + actual_away
        err_home  = result['pred_home']  - actual_home
        err_away  = result['pred_away']  - actual_away
        err_total = result['pred_total'] - actual_total
        abs_home  = abs(err_home)
        abs_away  = abs(err_away)
        abs_total = abs(err_total)

        if verbose:
            print(f"\n  ACTUAL RESULT")
            print(f"  {home_team}: {actual_home}  "
                  f"(predicted {result['pred_home']:.0f}, "
                  f"error: {err_home:+.1f})")
            print(f"  {away_team}: {actual_away}  "
                  f"(predicted {result['pred_away']:.0f}, "
                  f"error: {err_away:+.1f})")
            print(f"  Total: {actual_total}  "
                  f"(predicted {result['pred_total']:.0f}, "
                  f"error: {err_total:+.1f})")

            # Flag if we were badly wrong
            if abs_total > 25:
                print(f"\n  🚨 LARGE ERROR ({abs_total:.0f} pts) — "
                      f"investigate lineup/injury")
            elif abs_total > 15:
                print(f"\n  ⚠️  Above average error ({abs_total:.0f} pts)")
            else:
                print(f"\n  ✅ Good prediction (error: {abs_total:.0f} pts)")

    # ── Log the prediction ───────────────────────────────────
    new_row = pd.DataFrame([{
        'PRED_DATE'      : datetime.today().strftime('%Y-%m-%d'),
        'GAME_DATE'      : game_date,
        'HOME_TEAM'      : home_team,
        'AWAY_TEAM'      : away_team,
        'PRED_HOME'      : result['pred_home'],
        'PRED_AWAY'      : result['pred_away'],
        'PRED_TOTAL'     : result['pred_total'],
        'ACTUAL_HOME'    : actual_home,
        'ACTUAL_AWAY'    : actual_away,
        'ACTUAL_TOTAL'   : (actual_home + actual_away)
                            if actual_home else None,
        'HOME_INJURED'   : ','.join(home_out) if home_out else '',
        'AWAY_INJURED'   : ','.join(away_out) if away_out else '',
        'ERROR_HOME'     : err_home,
        'ERROR_AWAY'     : err_away,
        'ERROR_TOTAL'    : err_total,
        'ABS_ERROR_HOME' : abs_home,
        'ABS_ERROR_AWAY' : abs_away,
        'ABS_ERROR_TOTAL': abs_total,
        'CONFIDENCE'     : result['confidence'],
        'MODEL_VERSION'  : 'v5',
    }])

    pred_log = pd.concat([pred_log, new_row], ignore_index=True)
    pred_log.to_csv(log_path, index=False)

    return result

