def predict_game(home_team, away_team,
                 home_out=None, away_out=None,
                 verbose=True):
    """
    Predict score for any NBA matchup.
    Optionally flag injured/resting players.

    Args:
        home_team : e.g. 'BOS'
        away_team : e.g. 'LAL'
        home_out  : list of player names sitting out e.g. ['Tatum']
        away_out  : list of player names sitting out e.g. ['LeBron']
    """
    home_out = home_out or []
    away_out = away_out or []

    # ── Get team form ────────────────────────────────────────
    home_games = model_df[
        (model_df['HOME_TEAM']==home_team) &
        (model_df['SEASON']=='2024-25')
    ].sort_values('GAME_DATE')

    away_games = model_df[
        (model_df['AWAY_TEAM']==away_team) &
        (model_df['SEASON']=='2024-25')
    ].sort_values('GAME_DATE')

    if len(home_games) == 0:
        home_games = model_df[
            (model_df['AWAY_TEAM']==home_team) &
            (model_df['SEASON']=='2024-25')
        ].sort_values('GAME_DATE')

    if len(away_games) == 0:
        away_games = model_df[
            (model_df['HOME_TEAM']==away_team) &
            (model_df['SEASON']=='2024-25')
        ].sort_values('GAME_DATE')

    if len(home_games)==0 or len(away_games)==0:
        print(f"❌ No data for {home_team} or {away_team}")
        return None

    home_row = home_games.iloc[-1]
    away_row = away_games.iloc[-1]

    # ── Build feature vector ─────────────────────────────────
    feature_dict = {}
    for col in FEATURE_COLS:
        if col.startswith('AWAY_') and col in away_row.index:
            feature_dict[col] = away_row[col]
        elif col in home_row.index:
            feature_dict[col] = home_row[col]
        else:
            feature_dict[col] = 0

    X_pred = pd.DataFrame([feature_dict])[FEATURE_COLS]

    # ── Base predictions ─────────────────────────────────────
    pred_home  = rf_A2.predict(X_pred)[0]
    pred_away  = rf_B2.predict(X_pred)[0]
    pred_total = rf_C2.predict(X_pred)[0]

    # ── Injury adjustments ───────────────────────────────────
    s25 = player_impact_df[
        (player_impact_df['SEASON']=='2024-25') &
        (player_impact_df['GAMES_PLAYED'] >= 15)
    ].copy()

    injury_log = []

    # Home team injuries
    for player_name in home_out:
        matches = s25[
            (s25['TEAM_ABBR']==home_team) &
            (s25['PLAYER_NAME'].str.contains(
                player_name, case=False, na=False))
        ]
        if len(matches) > 0:
            impact = matches.iloc[0]['PTS_IMPACT']
            pred_home  -= impact
            pred_total -= impact
            injury_log.append(
                f"  {home_team} OUT: {matches.iloc[0]['PLAYER_NAME']} "
                f"→ -{impact:.1f} pts"
            )
        else:
            injury_log.append(
                f"  ⚠️  {player_name} not found in {home_team} roster")

    # Away team injuries
    for player_name in away_out:
        matches = s25[
            (s25['TEAM_ABBR']==away_team) &
            (s25['PLAYER_NAME'].str.contains(
                player_name, case=False, na=False))
        ]
        if len(matches) > 0:
            impact = matches.iloc[0]['PTS_IMPACT']
            pred_away  -= impact
            pred_total -= impact
            injury_log.append(
                f"  {away_team} OUT: {matches.iloc[0]['PLAYER_NAME']} "
                f"→ -{impact:.1f} pts"
            )
        else:
            injury_log.append(
                f"  ⚠️  {player_name} not found in {away_team} roster")

    # ── Win probability ──────────────────────────────────────
    margin   = pred_home - pred_away
    win_prob = 1 / (1 + np.exp(-margin / 10))

    if abs(margin) >= 12:   confidence = "HIGH"
    elif abs(margin) >= 7:  confidence = "MEDIUM"
    elif abs(margin) >= 3:  confidence = "LOW"
    else:                   confidence = "SKIP — too close"

    # ── Shot style ───────────────────────────────────────────
    def get_style(team):
        row = shot_df[
            (shot_df['TEAM_ABBR']==team) &
            (shot_df['SEASON']=='2024-25')
        ]
        if len(row)==0: return 0, 0, 0, 'Unknown'
        r = row.iloc[0]
        t, p = r['THREEPT_RATE'], r['PAINT_RATE']
        if t > 0.45:   label = "3PT-heavy"
        elif p > 0.52: label = "Paint-dominant"
        elif t > 0.40: label = "Balanced 3PT"
        else:          label = "Balanced"
        return t, p, r['PTS_PER_SHOT'], label

    ht, hp, hpps, hstyle = get_style(home_team)
    at, ap, apps, astyle = get_style(away_team)

    # ── Momentum ─────────────────────────────────────────────
    hm = home_row.get('HOME_MOMENTUM', 0)
    am = away_row.get('AWAY_MOMENTUM', 0)

    if verbose:
        inj_section = ""
        if injury_log:
            inj_section = "\n  INJURY ADJUSTMENTS\n" + \
                          "\n".join(injury_log)

        print(f"""
╔══════════════════════════════════════════════════════╗
  NBA AI SCORE PREDICTOR  |  v5 + Player Impact
  {home_team} (home)  vs  {away_team} (away)
╠══════════════════════════════════════════════════════╣
  PREDICTED FINAL SCORE
  {home_team}: {pred_home:.0f} pts   range: {round(pred_home-9.4)}–{round(pred_home+9.4)}
  {away_team}: {pred_away:.0f} pts   range: {round(pred_away-9.5)}–{round(pred_away+9.5)}
  Total  : {pred_total:.0f} pts   range: {round(pred_total-14.8)}–{round(pred_total+14.8)}

  WIN PROBABILITY
  {home_team}: {win_prob*100:.1f}%   {away_team}: {100-win_prob*100:.1f}%
  Confidence: {confidence}
{inj_section}
  TEAM FORM — last 10 games
  {home_team}  scored: {home_row['HOME_ROLL10_PTS']:.1f}  allowed: {home_row['HOME_DEF_ROLL10']:.1f}  momentum: {hm:+.1f}
  {away_team}  scored: {away_row['AWAY_ROLL10_PTS']:.1f}  allowed: {away_row['AWAY_DEF_ROLL10']:.1f}  momentum: {am:+.1f}

  SHOT STYLE
  {home_team}: {hstyle:<16} 3PT:{ht:.1%} Paint:{hp:.1%} PPS:{hpps:.3f}
  {away_team}: {astyle:<16} 3PT:{at:.1%} Paint:{ap:.1%} PPS:{apps:.3f}
╚══════════════════════════════════════════════════════╝""")

    return {
        'home_team' : home_team, 'away_team': away_team,
        'pred_home' : round(pred_home,1),
        'pred_away' : round(pred_away,1),
        'pred_total': round(pred_total,1),
        'win_prob'  : round(win_prob*100,1),
        'confidence': confidence,
    }

