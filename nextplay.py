# CELL 11 — predict_game() function
# ============================================================
# Takes any two team abbreviations and outputs a full
# prediction report using our trained models.
#
# Uses 2024-25 data as the "current form" baseline
# since that's the most recent season we have.
# ============================================================

def predict_game(home_team, away_team, verbose=True):
    """
    Predict the score of any NBA matchup.

    Args:
        home_team : team abbreviation e.g. 'BOS', 'LAL', 'GSW'
        away_team : team abbreviation e.g. 'NYK', 'DEN', 'MIA'
        verbose   : print full report (True) or return dict only (False)

    Returns:
        dict with all prediction values
    """

    # ── Get most recent form for each team ──────────────────
    # Use last available game from 2024-25 as the feature snapshot
    home_games = model_df[
        (model_df['HOME_TEAM'] == home_team) &
        (model_df['SEASON'] == '2024-25')
    ].sort_values('GAME_DATE')

    away_games = model_df[
        (model_df['AWAY_TEAM'] == away_team) &
        (model_df['SEASON'] == '2024-25')
    ].sort_values('GAME_DATE')

    # Fallback: check away appearances for home team
    if len(home_games) == 0:
        home_games = model_df[
            (model_df['AWAY_TEAM'] == home_team) &
            (model_df['SEASON'] == '2024-25')
        ].sort_values('GAME_DATE')

    if len(away_games) == 0:
        away_games = model_df[
            (model_df['HOME_TEAM'] == away_team) &
            (model_df['SEASON'] == '2024-25')
        ].sort_values('GAME_DATE')

    if len(home_games) == 0 or len(away_games) == 0:
        print(f"❌ Could not find data for {home_team} or {away_team}")
        print(f"   Valid teams: {sorted(model_df['HOME_TEAM'].unique())}")
        return None

    # Take the most recent game's rolling features
    home_row = home_games.iloc[-1]
    away_row = away_games.iloc[-1]

    # ── Build feature vector ─────────────────────────────────
    # Combine home team's home features with away team's away features
    feature_dict = {}

    for col in FEATURE_COLS:
        if col.startswith('HOME_') or col in [
            'COMBINED_PTS_ROLL10','COMBINED_FG3A_ROLL10',
            'COMBINED_FTA_ROLL10','TOV_DIFF_ROLL10',
            'OREB_DIFF_ROLL10','COMBINED_DEF_ROLL10',
            'EXPECTED_TOTAL','REST_DAYS_DIFF','STREAK_DIFF',
            'SEASON_STAGE','STRENGTH_DIFF'
        ]:
            feature_dict[col] = home_row[col] if col in home_row.index else 0
        elif col.startswith('AWAY_'):
            feature_dict[col] = away_row[col] if col in away_row.index else 0
        else:
            feature_dict[col] = home_row[col] if col in home_row.index else 0

    X_pred = pd.DataFrame([feature_dict])[FEATURE_COLS]

    # ── Generate predictions ─────────────────────────────────
    pred_home  = rf_A2.predict(X_pred)[0]
